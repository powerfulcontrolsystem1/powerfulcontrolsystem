func TestCodigoDescuentoRedencionSeRevierteAlReactivarCarrito(t *testing.T) {
	dbConn := openCarritoInventarioTestDB(t)
	if err := EnsureEmpresaCarritosSchema(dbConn); err != nil {
		t.Fatalf("ensure carritos schema: %v", err)
	}
	if err := EnsureEmpresaCodigosDescuentoSchema(dbConn); err != nil {
		t.Fatalf("ensure codigos descuento schema: %v", err)
	}

	carritoID, err := CreateCarritoCompra(dbConn, CarritoCompra{
		EmpresaID:      1,
		Codigo:         "CAR-DESC-01",
		Nombre:         "Carrito Desc",
		CanalVenta:     "mostrador",
		ClienteID:      123,
		Moneda:         "COP",
		UsuarioCreador: "test",
		Estado:         "activo",
	})
	if err != nil {
		t.Fatalf("create carrito: %v", err)
	}
	if _, err := dbConn.Exec(`UPDATE carritos_compras SET total = 20000, subtotal = 20000 WHERE empresa_id = 1 AND id = ?`, carritoID); err != nil {
		t.Fatalf("set carrito totals: %v", err)
	}

	vence := time.Now().Add(24 * time.Hour).Format("2006-01-02")
	id, err := CreateCodigoDescuento(dbConn, CodigoDescuento{
		EmpresaID:         1,
		Codigo:            "TRACE01",
		TipoDescuento:     "valor_fijo",
		Valor:             5000,
		Moneda:            "COP",
		MontoMinimoCompra: 10000,
		FechaVencimiento:  vence,
		UsosMaximos:       10,
		UsuarioCreador:    "test",
	})
	if err != nil {
		t.Fatalf("create trace code: %v", err)
	}
	item, err := GetCodigoDescuentoByID(dbConn, 1, id)
	if err != nil {
		t.Fatalf("get trace code: %v", err)
	}

	aplicado, err := ResolveCodigoDescuentoParaMontoConContexto(dbConn, 1, item.Codigo, 20000, CodigoDescuentoContexto{
		CarritoID:  carritoID,
		ClienteID:  123,
		CanalVenta: "mostrador",
	})
	if err != nil {
		t.Fatalf("resolve trace code: %v", err)
	}

	if err := PayCarritoStationSession(dbConn, 1, carritoID, "efectivo", "REF-TRACE", "code", item.Codigo, aplicado.ValorAplicado, 0, 15000, id, "test"); err != nil {
		t.Fatalf("pay carrito with code: %v", err)
	}

	var usosTrasPago int64
	if err := dbConn.QueryRow(`SELECT COALESCE(usos_actuales, 0) FROM codigos_de_descuento WHERE empresa_id = 1 AND id = ?`, id).Scan(&usosTrasPago); err != nil {
		t.Fatalf("query usos after pay: %v", err)
	}
	if usosTrasPago != 1 {
		t.Fatalf("expected usos_actuales=1 after pay, got %d", usosTrasPago)
	}

	if err := ActivateCarritoStationSession(dbConn, 1, carritoID, false); err != nil {
		t.Fatalf("activate carrito session: %v", err)
	}

	var usosTrasRevert int64
	if err := dbConn.QueryRow(`SELECT COALESCE(usos_actuales, 0) FROM codigos_de_descuento WHERE empresa_id = 1 AND id = ?`, id).Scan(&usosTrasRevert); err != nil {
		t.Fatalf("query usos after revert: %v", err)
	}
	if usosTrasRevert != 0 {
		t.Fatalf("expected usos_actuales=0 after revert, got %d", usosTrasRevert)
	}

	rows, err := ListCodigoDescuentoRedencionesByEmpresa(dbConn, 1, id, "", 20)
	if err != nil {
		t.Fatalf("list redenciones: %v", err)
	}
	if len(rows) == 0 {
		t.Fatalf("expected at least one redencion record")
	}
	if strings.TrimSpace(strings.ToLower(rows[0].EstadoRedencion)) != "revertida" {
		t.Fatalf("expected latest redencion state revertida, got %q", rows[0].EstadoRedencion)
	}
}
